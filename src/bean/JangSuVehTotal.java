package bean;

public class JangSuVehTotal {
	
	private int year;
	private int total;
	public JangSuVehTotal(int year, int total) {
		super();
		this.year = year;
		this.total = total;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}


}
